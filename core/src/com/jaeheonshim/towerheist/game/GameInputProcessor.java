package com.jaeheonshim.towerheist.game;

import com.badlogic.gdx.Gdx;
import com.badlogic.gdx.Input;
import com.badlogic.gdx.InputProcessor;
import com.badlogic.gdx.math.Vector2;

public class GameInputProcessor implements InputProcessor {
    private GameScreen gameScreen;
    private GameWorld gameWorld;

    public GameInputProcessor(GameScreen gameScreen, GameWorld gameWorld) {
        this.gameScreen = gameScreen;
        this.gameWorld = gameWorld;
    }

    @Override
    public boolean keyDown(int keycode) {
        return false;
    }

    @Override
    public boolean keyUp(int keycode) {
        return false;
    }

    @Override
    public boolean keyTyped(char character) {
        return false;
    }

    @Override
    public boolean touchDown(int screenX, int screenY, int pointer, int button) {
        gameWorld.getPlayer().setPosition(gameScreen.getGameCamera().getViewport().unproject(new Vector2(screenX, screenY)));
        return true;
    }

    @Override
    public boolean touchUp(int screenX, int screenY, int pointer, int button) {
        return false;
    }

    @Override
    public boolean touchDragged(int screenX, int screenY, int pointer) {
        return false;
    }

    @Override
    public boolean mouseMoved(int screenX, int screenY) {
        return false;
    }

    @Override
    public boolean scrolled(float amountX, float amountY) {
        return false;
    }
}
